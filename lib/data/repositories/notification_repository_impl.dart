  @override
  Future<Either<Failure, Notification>> sendNotification({
    required String title,
    required String message,
    required String type,
    required String recipientEmail,
    String? taskId,
    Map<String, dynamic>? data,
    String? actionUrl,
  }) async {
    try {
      if (await networkInfo.isConnected) {
        final notification = NotificationModel(
          notificationId: '', // Will be generated by Firestore
          title: title,
          message: message,
          type: type,
          recipientEmail: recipientEmail,
          taskId: taskId,
          data: data,
          createdAt: DateTime.now(),
          isRead: false,
          actionUrl: actionUrl,
        );

        final createdNotification = await remoteDataSource.createNotification(
          notification,
        );

        // Individual notifications don't trigger push notifications
        // Push notifications are handled at the sendNotificationToMultiple level
        // to avoid sending duplicates for each recipient

        return Right(createdNotification.toEntity());
      } else {
        return const Left(NetworkFailure(message: 'No internet connection'));
      }
    } on ServerException catch (e) {
      return Left(ServerFailure(message: e.message));
    } catch (e) {
      return Left(ServerFailure(message: 'Failed to send notification: $e'));
    }
  }

  @override
  Future<Either<Failure, List<Notification>>> sendNotificationToMultiple({
    required String title,
    required String message,
    required String type,
    required List<String> recipientEmails,
    String? taskId,
    Map<String, dynamic>? data,
    String? actionUrl,
  }) async {
    try {
      if (await networkInfo.isConnected) {
        final notifications = <Notification>[];

        // Send notifications to each recipient
        for (final email in recipientEmails) {
          final result = await sendNotification(
            title: title,
            message: message,
            type: type,
            recipientEmail: email,
            taskId: taskId,
            data: data,
            actionUrl: actionUrl,
          );

          result.fold(
            (failure) => throw Exception(failure.message),
            (notification) => notifications.add(notification),
          );
        }

        // Send push notification ONCE after all individual notifications are created
        // This ensures we don't send duplicate push notifications for each recipient
        if (type == 'new_task' && taskId != null && data != null) {
          await _sendPushNotificationForNewTask(
            taskId: taskId,
            title: title,
            message: message,
            data: data,
          );
        }

        return Right(notifications);
      } else {
        return const Left(NetworkFailure(message: 'No internet connection'));
      }
    } catch (e) {
      return Left(ServerFailure(message: 'Failed to send notifications: $e'));
    }
  }

  @override
  Future<Either<Failure, List<Notification>>>
  sendNotificationToManagersAndAdmins({
    required String title,
    required String message,
    required String type,
    String? taskId,
    String? taskCategory, // New parameter for category filtering
    Map<String, dynamic>? data,
    String? actionUrl,
  }) async {
    try {
      // Get all managers
      final managersResult = await userRepository.getManagers();

      return await managersResult.fold((failure) => Left(failure), (
        managers,
      ) async {
        // Filter managers by task category (if provided)
        List<dynamic> filteredManagers = managers;
        if (taskCategory != null) {
          filteredManagers = managers.where((manager) {
            // If manager has managedCategories, check if it includes the task category
            // If managedCategories is null or empty, manager handles all categories (backward compatibility)
            if (manager.managedCategories == null ||
                manager.managedCategories!.isEmpty) {
              return true; // Handle all categories
            }
            return manager.managedCategories!.contains(taskCategory);
          }).toList();
        }

        // Get all admins (admins always receive all notifications)
        final adminsResult = await userRepository.getUsersByRole(role: 'Admin');

        return await adminsResult.fold((failure) => Left(failure), (
          admins,
        ) async {
          // Combine filtered managers and all admins, remove duplicates
          final allRecipients = <String>{};
          for (final manager in filteredManagers) {
            allRecipients.add(manager.userEmail);
          }
          for (final admin in admins) {
            allRecipients.add(admin.userEmail);
          }

          // Send notifications
          final result = await sendNotificationToMultiple(
            title: title,
            message: message,
            type: type,
            recipientEmails: allRecipients.toList(),
            taskId: taskId,
            data: data,
            actionUrl: actionUrl,
          );

          // Push notification is already sent by sendNotificationToMultiple above
          // No need to call _sendPushNotificationForNewTask again here

          return result;
        });
      });
    } catch (e) {
      return Left(
        ServerFailure(
          message: 'Failed to send notifications to managers and admins: $e',
        ),
      );
    }
  }

