      }
    } on ServerException catch (e) {
      return Left(ServerFailure(message: e.message));
    } catch (e) {
      return Left(
        ServerFailure(message: 'Failed to mark notification as read: $e'),
      );
    }
  }

  @override
  Future<Either<Failure, void>> markAllAsRead(String userEmail) async {
    try {
      if (await networkInfo.isConnected) {
        // Get all unread notifications for the user
        final notifications = await remoteDataSource.getNotificationsForUser(
          userEmail: userEmail,
        );

        // Update each unread notification
        for (final notification in notifications) {
          if (!notification.isRead) {
            await remoteDataSource.updateNotification(
              notification.notificationId,
              {'isRead': true},
            );
          }
        }

        return const Right(null);
      } else {
        return const Left(NetworkFailure(message: 'No internet connection'));
      }
    } on ServerException catch (e) {
      return Left(ServerFailure(message: e.message));
    } catch (e) {
      return Left(
        ServerFailure(message: 'Failed to mark all notifications as read: $e'),
      );
    }
  }

